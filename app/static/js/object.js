function hidehost() {
    var form = document.getElementById("hostform");
    if (form.style.display == "none") {
        form.style.display = "block";
    } else if (form.style.display == "block") {
        form.style.display = "none";
    }
}

function hidenet() {
    var form = document.getElementById("netform");
    if (form.style.display == "none") {
        form.style.display = "block";
    } else if (form.style.display == "block") {
        form.style.display = "none";
    }
}

